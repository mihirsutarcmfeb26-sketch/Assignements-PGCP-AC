class Type {

    public static void main(String[] args) {

        int a = 10;
        double b = 3.5;

        // Addition
        double add = a + b;

        // Division
        double div = a / b;

        // Type casting to int
        int result = (int) div;

        System.out.println("Addition: " + add);
        System.out.println("Division: " + div);
        System.out.println("After Type Casting (int): " + result);
    }
}
